#!/usr/bin/env bash

[[ -n "${_base_repo_installer_template_sourced:-}" ]] && return 0
_base_repo_installer_template_sourced=1
readonly _base_repo_installer_template_sourced

base_repo_installer_template_usage() {
    cat <<'EOF'
Usage:
  basectl repo installer-template [path] [options]

Options:
  --print, --stdout             Print the maintained template to stdout instead of writing a file.
  --repo <owner/name>           GitHub repository for --pr. Defaults to the target origin remote.
  --issue <number>              Issue number for --pr. Required with --pr.
  --category <name>             Issue category for --pr --dry-run. Real PR runs derive or verify it.
  --pr                          Commit the generated installer template on a branch and open a draft pull request.
  --dry-run                     Print planned changes without applying them.
  -v                            Enable DEBUG logging for this subcommand.
  -h, --help                    Show this help text.

Writes the maintained project installer template to path, or to ./install.sh
when path is omitted. Use --print to inspect or pipe the template.
With --pr --dry-run, pass --category because dry-run performs no GitHub reads.
Real PR runs derive the issue's standard category label and verify --category
when it is supplied.
EOF
}

base_repo_installer_template_path() {
    [[ -n "${BASE_HOME:-}" ]] || {
        log_error "BASE_HOME is required to locate the project installer template."
        return 1
    }

    printf '%s/templates/project-install.sh\n' "$BASE_HOME"
}

base_repo_print_installer_template() {
    local template

    template="$(base_repo_installer_template_path)" || return 1
    [[ -f "$template" ]] || {
        log_error "Project installer template was not found at '$template'."
        return 1
    }

    cat "$template"
}

base_repo_write_installer_template() {
    local dry_run="$1"
    local target="$2"
    local template

    template="$(base_repo_installer_template_path)" || return 1
    [[ -f "$template" ]] || {
        log_error "Project installer template was not found at '$template'."
        return 1
    }

    base_repo_write_executable_stream "$dry_run" "$target" < "$template"
}

base_repo_create_installer_template_pr_body() {
    local category="$4"
    local issue="$3"
    local path="$1"
    local repo="$2"

    cat <<EOF
## Summary

- Add the maintained Base project installer template.

## Issue

Closes #$issue

## Validation

- Not run. Generated installer-template change only.

Generated by:

\`\`\`bash
basectl repo installer-template $(base_repo_pretty_arg "$path") --repo $repo --issue $issue --category $category --pr
\`\`\`
EOF
}

base_repo_finish_installer_template_pr() {
    local dry_run="$1"
    local target_path="$2"
    local root="$3"
    local repo="$4"
    local branch="$5"
    local default_branch="$6"
    local rel_path="$7"
    local issue="$8"
    local category="$9"
    local body_file status

    if [[ "$dry_run" == "1" ]]; then
        base_repo_finish_generated_pr \
            "$dry_run" \
            "$root" \
            "$repo" \
            "$branch" \
            "$default_branch" \
            "Add Base installer template" \
            "installer template file" \
            "Add Base installer template" \
            "" \
            "$rel_path"
        return $?
    fi

    std_make_temp_file body_file base-repo-installer-template-pr || {
        log_error "Failed to create a temporary pull request body file."
        return 1
    }
    base_repo_create_installer_template_pr_body "$target_path" "$repo" "$issue" "$category" > "$body_file"
    base_repo_finish_generated_pr \
        "$dry_run" \
        "$root" \
        "$repo" \
        "$branch" \
        "$default_branch" \
        "Add Base installer template" \
        "installer template file" \
        "Add Base installer template" \
        "$body_file" \
        "$rel_path"
    status=$?
    rm -f "$body_file"
    return "$status"
}

base_repo_installer_template() {
    local category=""
    local create_pr=0
    local dry_run=0
    local github_repo=""
    local issue=""
    local issue_category=""
    local path=""
    local print_template=0
    local pr_branch=""
    local pr_category=""
    local pr_default_branch=""
    local rel_path=""
    local repo_name=""
    local root=""

    while (($#)); do
        case "$1" in
            -h|--help|help)
                base_repo_installer_template_usage
                return 0
                ;;
            --repo)
                [[ -n "${2:-}" ]] || {
                    base_repo_installer_template_usage_error "Option '--repo' requires an argument."
                    return $?
                }
                github_repo="$2"
                shift 2
                ;;
            --repo=*)
                github_repo="${1#--repo=}"
                [[ -n "$github_repo" ]] || {
                    base_repo_installer_template_usage_error "Option '--repo' requires an argument."
                    return $?
                }
                shift
                ;;
            --issue)
                [[ -n "${2:-}" ]] || {
                    base_repo_installer_template_usage_error "Option '--issue' requires a positive integer argument."
                    return $?
                }
                issue="$2"
                shift 2
                ;;
            --issue=*)
                issue="${1#--issue=}"
                [[ -n "$issue" ]] || {
                    base_repo_installer_template_usage_error "Option '--issue' requires a positive integer argument."
                    return $?
                }
                shift
                ;;
            --category)
                [[ -n "${2:-}" ]] || {
                    base_repo_installer_template_usage_error "Option '--category' requires an argument."
                    return $?
                }
                category="$2"
                shift 2
                ;;
            --category=*)
                category="${1#--category=}"
                [[ -n "$category" ]] || {
                    base_repo_installer_template_usage_error "Option '--category' requires an argument."
                    return $?
                }
                shift
                ;;
            --print|--stdout)
                print_template=1
                shift
                ;;
            --pr)
                create_pr=1
                shift
                ;;
            --dry-run)
                dry_run=1
                shift
                ;;
            -v)
                set_log_level DEBUG
                export LOG_DEBUG=1
                shift
                ;;
            -*)
                base_repo_installer_template_usage_error "Unknown repo installer-template option '$1'."
                return $?
                ;;
            *)
                if [[ -n "$path" ]]; then
                    base_repo_installer_template_usage_error "The 'repo installer-template' command accepts at most one path."
                    return $?
                fi
                path="$1"
                shift
                ;;
        esac
    done

    if ((print_template)); then
        if ((create_pr)); then
            base_repo_installer_template_usage_error "Option '--print' cannot be used with --pr."
            return $?
        fi
        if [[ -n "$path" ]]; then
            base_repo_installer_template_usage_error "Option '--print' cannot be used with a path."
            return $?
        fi
        base_repo_print_installer_template
        return $?
    fi

    [[ -n "$path" ]] || path="install.sh"

    if [[ -n "$issue" ]] && ! base_github_issue_number_is_valid "$issue"; then
        base_repo_installer_template_usage_error "Option '--issue' must be a positive integer."
        return $?
    fi
    if [[ -n "$category" ]] && ! base_github_branch_category_is_valid "$category"; then
        base_repo_installer_template_usage_error "Option '--category' must be one of: bug, enhancement, documentation, ci, security."
        return $?
    fi

    path="$(base_repo_target_path "$path")"
    root="$(dirname -- "$path")"
    repo_name="$(basename -- "$root")"

    if ((create_pr)); then
        [[ -n "$issue" ]] || {
            base_repo_installer_template_usage_error "Option '--pr' requires --issue <positive integer>."
            return $?
        }
        if [[ -z "$github_repo" ]]; then
            github_repo="$(base_repo_infer_github_repo "$root" || true)"
        fi
        [[ -n "$github_repo" ]] || {
            base_repo_installer_template_usage_error "Option '--pr' requires --repo <owner/name> or an inferable GitHub origin remote."
            return $?
        }

        repo_name="${github_repo#*/}"
        if [[ "$dry_run" == "1" ]]; then
            [[ -n "$category" ]] || {
                base_repo_installer_template_usage_error "Options '--pr --dry-run' require --category <name>."
                return $?
            }
            pr_category="$category"
        else
            base_repo_require_pr_worktree "$root" "repo installer-template --pr" || return 1
            rel_path="$(base_repo_relative_path_under_root "$root" "$path")" || {
                log_error "repo installer-template --pr expects path to be inside '$root'."
                return 1
            }
            issue_category="$(base_repo_pr_issue_category "$github_repo" "$issue")" || return 1
            if [[ -n "$category" && "$category" != "$issue_category" ]]; then
                base_repo_installer_template_usage_error "Option '--category $category' does not match issue #$issue category '$issue_category'."
                return $?
            fi
            pr_category="$issue_category"
        fi
        pr_branch="$(base_repo_pr_branch_name "$pr_category" "$issue" "installer-template" "$repo_name")" || {
            log_error "Unable to generate the canonical issue branch for repo installer-template --pr."
            return 1
        }
        if [[ "$dry_run" == "1" ]]; then
            pr_default_branch="<default branch>"
            rel_path="$(basename -- "$path")"
        else
            pr_default_branch="$(base_repo_default_branch_for_pr "$github_repo")" || return 1
        fi
        base_repo_prepare_pr_branch "$dry_run" "$root" "$pr_branch" "$pr_default_branch" "repo installer-template --pr" || return 1
    fi

    base_repo_write_installer_template "$dry_run" "$path" || return $?
    if ((create_pr)); then
        base_repo_finish_installer_template_pr \
            "$dry_run" \
            "$path" \
            "$root" \
            "$github_repo" \
            "$pr_branch" \
            "$pr_default_branch" \
            "$rel_path" \
            "$issue" \
            "$pr_category"
        return $?
    fi
    if [[ "$dry_run" != "1" ]]; then
        base_repo_print_review_hint "$(dirname -- "$path")"
    fi
}
